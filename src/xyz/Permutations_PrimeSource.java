package xyz;

import java.util.*;

public class Permutations_PrimeSource {

    // Рекурсивная функция для генерации всех перестановок
    private static void generatePermutations(int[] arr, int index) {
        if (index == arr.length - 1) {
            check(arr);
            return;
        }

        for (int i = index; i < arr.length; i++) {
            // Обмен текущего элемента с элементом на текущей позиции
            int temp = arr[index];
            arr[index] = arr[i];
            arr[i] = temp;

            // Рекурсивно генерируем перестановки для подмассива
            generatePermutations(arr, index + 1);

            // Возврат исходного состояния массива
            temp = arr[index];
            arr[index] = arr[i];
            arr[i] = temp;
        }
    }

    public static void check(int[] a) {
        int left = a[0] * (a[1] * 10 + a[2]);
        int right = a[8] * (a[6] * 10 + a[7]);
        int centre = (a[3] * 100) + (a[4] * 10) + a[5];
        if (left == right && right == centre) System.out.println(Arrays.toString(a));
    }

    public static void main(String[] args) {
        int[] array = {7, 2, 8, 1, 9, 6, 3, 4, 5};
        generatePermutations(array, 0);
    }
}
